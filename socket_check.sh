#!/bin/bash
NAME=myLittleWebserv
while [ 1 ]
do
	
	echo "========================================================================================================================================================================================================================="
	PID=$(ps u | grep -E '[.]/'${NAME} | awk '{print $2}' | xargs echo )
	lsof -p $PID 2> /dev/null | grep TCP
	echo ""
	ss | head -1
	ss | grep 127.0.0.1
	echo "========================================================================================================================================================================================================================="
	sleep 3
done
